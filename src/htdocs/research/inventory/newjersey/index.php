  		<p>We have recently completed an inventory of landslides in New Jersey and created a database with the information.  The data is available to the public for free via web download from the New Jersey Geological Survey (NJGS) web site. The data is compiled into an ESRI Geographic Information System (GIS) shapefile (Landslides in New Jersey). This GIS point shapefile of landslides in New Jersey with associated database file, projection file and metadata, contains point locations and other attributes for 160 landslide locations in New Jersey mapped by the NJGS.  The information for the landslide sites mapped was gathered from geologic studies, geologic reports, newspaper articles, weather reports, historical documents, masters theses and internet searches.  The landslide database was originally created for emergency management agencies in New Jersey to help plan disaster responses.</p>

  		<p>New Jersey is also involved in mapping landslide potential as part of the (Earthquake Loss Estimation Study For New Jersey: Geologic Component).  The NJGS, with partial funding from the N.J. State Police Office of Emergency Management, maps seismic soil properties, including shaking behavior, liquefaction susceptibility, and tendency to landslide. This is completed by county at 1:24,000 scale and is available as Adobe .pdf files.</p>
