<h3>Landslide Inventory in Kentucky</h3>

<p>The goal of this project is to provide a comprehensive framework and understanding of landslide data for Kentucky. There is a wealth of landslide inventory information that comes from a variety of sources; in the form of hard-copy maps, reports, and digital databases. Cooperating with other state offices, private businesses, and other state surveys to collect these types of data will provide an outlet for this valuable information.</p>
