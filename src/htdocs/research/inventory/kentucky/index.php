<p>Since the early 1970's, the Kentucky Transportation Cabinet and the Kentucky Transportation Center has received reports of approximately 3,000 landslides. Costs for repair of landslides exceeds $2 million annually. Thousands of slides are unrelated to transportation, however, and many are unreported. These also pose significant hazards to people and infrastructure. The Kentucky State Emergency Management office has spent $617,466 on acquisitions of landslide-prone homes from 2005 to 2007.</p>

<p>Kentucky's landslides occur in all regions of the state, mostly in the Ohio River Valley, the Knobs, the Outer Bluegrass, and the Eastern Kentucky Coal Field.</p>


