<p><strong>Landslide Mapping By The California Geological Survey</strong></p>

<p>Over the past forty years, the California Geological Survey (CGS) has prepared hundreds of landslide maps in different areas at a variety of map scales. Systematic landslide mapping by CGS began in the 1970's as part of cooperative projects with federal, other state, and local government agencies. It has continued to the present under state legislative acts prompted by extensive landslide damage caused by significant storms and earthquakes.</p>

