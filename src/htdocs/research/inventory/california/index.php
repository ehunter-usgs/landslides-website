<p>The principal types of landslide-related maps and reports by CGS include:</p>

<ul>
	<li>
    	<strong>Landslide Hazard Identification Maps</strong>
        <ul>
            <li>Typically 1:24,000 in scale, comprised of three types of maps (landslide features, landslide susceptibility and debris-flow susceptibility). Produced from 1986 through 1995, as mandated by the now-repealed Landslide Hazard Mapping Act.</li>
		</ul>
    </li>
	<li>
    	<strong>Watershed Maps</strong>
        <ul>
            <li>Typically 1:24,000 in scale, landslide features maps developed to assist in timber harvest planning and water quality protection. Some maps have overprints indicating steepness of slopes. Most are available for downloading in PDF format or on CD-ROM with GIS data.</li>
        </ul>
	</li>
    <li>
		<strong>"Geology for Planning" Products</strong>
        <ul>
            <li>Maps and accompanying reports that detail landslide features. Most of these products were produced at the request of city, county, or state agencies on a cost-sharing basis. Each product is uniquely designed to meet the needs of the contracting agency. Scale and map content vary. Most show landslide features and geology; some show landslide susceptibility.</li>
        </ul>
	</li>
    <li>
		<strong>Regional Geologic Maps</strong>
        <ul>
            <li>Scales vary. These principally are geologic maps; some show selected landslide features.</li>
        </ul>
	</li>
    <li>
		<strong>Seismic Hazard Zone Maps</strong>
		<ul>
            <li>These are 1:24,000-scale regulatory maps issued under the Seismic Hazard Mapping Act. Unlike the other maps listed herein, SHZ maps delineate areas (potential earthquake-induced landslide hazard zones) where site-specific investigations of landslide hazards are required by law prior to development.</li>
		</ul>
	</li>
    <li>
		<strong>Landslide Inventory Map Series</strong>
		<ul>
            <li>Landslide inventory maps prepared for Seismic Hazard Zonation at a scale of 1:24,000 are now available in PDF format as part of this new CGS map publication series.</li>
        </ul>
	</li>
    <li>
		<strong>Emergency Response Products</strong>
		<ul>
        	<li>Landslide-related products consisting of reports and maps that document landslide occurrences following major disasters or significant events. Post-event reports and maps have been summarized as Open-File Reports, articles in California Geology Magazine, and on CGS's web pages.</li>
        </ul>
	</li>
</ul>

