  <h3>Inventory Status for Washington</h3>
		<div class="row">
			<div class="column one-of-one">

				<p>Washington has an inventory for both landslides and landforms.  We keep these data in a GIS-based database (ArcGIS-SDE-Oracle).  The Division of Geology and Earth Resources has a prototype database for tracking losses and other information. Our databases are stored in an ArcGIS format and are accessible via web download and ArcIMS.  The Washington State Department of Transportation maintains a separate database for landslides occurring along transportation infrastructure.</p>
