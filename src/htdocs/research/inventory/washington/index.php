  <h3>Introduction of Landslide Activities in Washington</h3>
		<div class="row">
			<div class="column one-of-one">

				<figure class="left" style="width:290px">
					<img src="images/pe_ell.jpg" width="280" height="210" alt="Pe Ell Landslide" />
					<figcaption>The Pe Ell landslide occurred on December 3rd, 2007 - It caused a blockage of State Highway 6 and destruction of two houses and a garage.</figcaption>
				</figure>

				<p>We are currently in the middle of a statewide landslide hazard zonation mapping project, wherein all state and privately owned timber lands are inventoried (mapped) for landslides and areas of landslide hazard, using an established mapping protocol and resolution.  These maps are made available to the public for free via web download.  The data is compiled into two GIS databases (landslide inventory and landslide hazard zones) that are also made freely available to the public via web download.  In order to create a landslide data repository for the state, these data were compiled with landslide inventories and landform maps that were created by other sources, in some cases for other purposes.  It is not known if the local government entities that issue development permits are using these data, but have been encouraged to do so, as many of them have critical areas ordinances requiring geotechnical input for developing on unstable or potentially unstable ground.</p>

				<p>The state has requirements to avoid harvesting timber on unstable slopes if doing so would increase the likelihood of failure and delivery of debris to public resources or would threaten public safety.  As such, these maps are used by land managers, regulators, and the geotechnical community in developing acceptable harvest plans.  The utility of this mapping project was identified during a set of negotiations that led to new unstable slopes rules.</p>
