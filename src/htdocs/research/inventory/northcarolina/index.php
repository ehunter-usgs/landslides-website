<h3>Introduction to Landslide Activities in North Carolina</h3>
	<figure class="left" style="width:280px">
		<a href="images/blue_ridge_2006.jpg"><img src="images/blue_ridge_2006_sm.jpg" alt="Blue Ridge Parkway 2006 Rock Slide" /></a>
		<figcaption>North Carolina also has problems with rock falls and rock slides. This slide occurred along the Blue Ridge Parkway in 2006 totaling a passing vehicle and injuring the passengers.</figcaption>
	</figure>

	<p>There is a significant slope movement hazard in North Carolina, mainly in the mountainous western region.  Recent and historical evidence shows that major damaging debris flow events have affected the region at least four times from 1916 to 2004.  The North Carolina Geological Survey (NCGS) has undertaken a major mapping program to develop slope movement hazard maps for 19 western North Carolina counties.  This work was authorized and funded by the N.C. General Assembly in the Hurricane Recovery Act of 2005; however, at present the maps are informational and not regulatory.  In addition to mapping, NCGS staff respond to technical assistance requests from local governments and the N.C. Division of Emergency Management in landslide instances that affect public safety and provide educational outreach to citizens and governmental agencies.</p>

	<p>For each county, the NCGS creates a series of GIS maps that show slope movements and slope movement deposit locations, relative hazard rankings for debris flow initiation areas, and downslope hazard areas for potential debris flow pathways. An aggressive mapping schedule calls for completing 2-3 counties per year.</p>

