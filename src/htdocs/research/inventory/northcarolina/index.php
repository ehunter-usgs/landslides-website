<h3>Inventory Status for North Carolina</h3>
	<figure class="left" style="width:280px">
		<a href="images/watuaga_county_2004.jpg"><img src="images/watuaga_county_2004_sm.jpg" alt="Watuaga County 2004 Hurricane Frances Landslide" /></a>
		<figcaption>A landslide (embankment failure / debris flow) in a subdivision in Watuaga County, NC near Boone.  One home was destroyed and eight others condemned for occupancy by landslides related to Hurricane Frances in 2004.</figcaption>
	</figure>

	<p>The NCGS actively maintains a landslide inventory (slope movement-slope movement deposit database) with over 5,600 data points.  The database is currently in a MS Access format, but we plan to migrate to a GIS-geodatabase in the near future. Database entry information includes categories for location, landslide and deposit (e.g., debris fans) type, displaced mass dimensions, damage, movement history, slope configuration, geomorphology, lithology, Unified Rock Classification System, bedrock structure, soil classification and thickness, vegetation, surface water, groundwater features and others.</p>

	<p>We have completed landslide hazard maps for two NC counties: Macon and Watauga. Our current work focuses on Buncombe County, with a projected completion time in Fall 2008.</p>
