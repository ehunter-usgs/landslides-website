		<p>One of the most common and devastating geologic hazards in Oregon is landslides. Average annual repair costs for landslides in Oregon exceed $10 million, and severe winter storm losses can exceed $100 million (Wang and others, 2002). In the past ten years, Oregon has witnessed significant individual landslide events, like the Newell Creek Apartment complex in Oregon City, the landslide in downtown Astoria, debris flows on Mount Hood, and the debris flow at Woodson; these events are likely increasing the estimated average and severe winter storm losses. As population growth continues and as development into landslide susceptible terrain occurs, greater losses are likely to result. To reduce losses from landslides, planning and action are necessary at all community levels from state government to individual families. One successful way to reduce losses from landslides is through pre-disaster mitigation, which can be performed at many scales from statewide to local. Before pre-disaster mitigation can occur, the landslide hazard must be located. Once the hazard is located, the population and infrastructure vulnerable to the hazards can be identified and the risk mitigated.</p>
