<h3>Inventory Status for Oregon</h3>
<div class="row">
	<div class="column one-of-one">

		<p>The Oregon Department of Geology and Mineral Industries (DOGAMI), along with other Oregon state agencies, local governments, and federal agencies, has been studying and preparing maps with landslides and landslide hazards in Oregon since the 1950s. Most of this work has been published on paper maps and recently has been digitized and put into a database as the Statewide Landslide Information Database for Oregon (SLIDO), discussed below.  Along with SLIDO, DOGAMI is engaged in several other efforts to inventory landslides in Oregon.</p>

		<p>Another publication from DOGAMI that catalogs landslides is Special Paper 34, Slope failures in Oregon, GIS inventory for three storm events in 1996-97 (Hofmeister, 2000). The objective of this project was to collect and consolidate data on Oregon landslides associated with severe storm events in February 1996, November 1996, and December 1996/January 1997. The February storm event led to a Federal disaster declaration for 27 counties, the November event for 3 counties, and the December/January storms for 14 counties. The inventory database includes over 9,500 slide location entries. The database entries contain several items describing the geographic location of each landslide and up to 15 additional items relating to failure mechanism, size, geometry, associated damage, etc., depending upon the information obtained from the contributing sources. The digital outputs are intended to provide a starting point for future landslide-related studies. This publication is also available on the DOGAMI web site.</p>

		<p>Currently DOGAMI is producing lidar-based landslide inventories for small portions of Oregon.  The first of these maps was released in 2006, LIDAR-Based Landslide Geomorphology Map of Oregon City (Open File Report O-06-27). A pilot project area was selected to compare remote sensing data/images for effectiveness (Burns, 2007).  Two key findings from this pilot study were: 1) the use of the light detection and ranging (lidar) data resulted in the identification of between 3 to 200 times the number of landslides found with the other data sets, and 2) the accuracy of the spatial extent of the landslides identified was greatly improved with the lidar data.</p>

